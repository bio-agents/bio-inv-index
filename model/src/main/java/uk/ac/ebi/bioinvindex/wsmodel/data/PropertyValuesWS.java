import uk.ac.ebi.bioinvindex.wsmodel.enumerations.PropertyType;
import java.io.Serializable;

/**
 * This class is a resume of all the properties that can exist
 * @author: Manon DELAHAYE [manon@ebi.ac.uk]
 * Date: 07-Apr-2009
 */
public class PropertyValuesWS implements Serializable {

    /* Examples : characteristic, organism, dog
     *            factor, ,15,
     */

    /* The type of the property which can be one in the enumeration */
    private PropertyType type;

    /* The name of the property. */
    private String name;

    /* The vqlue of the property. */
    private String value;

    /* The unit of the property's value if needed. */
    private String unit;

    public PropertyValuesWS()
    {
        
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public PropertyType getType() {
        return type;
    }

    public void setType(PropertyType type) {
        this.type = type;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        PropertyValuesWS that = (PropertyValuesWS) o;

        if (name != null ? !name.equals(that.name) : that.name != null) return false;
        if (type != that.type) return false;
        if (unit != null ? !unit.equals(that.unit) : that.unit != null) return false;
        if (value != null ? !value.equals(that.value) : that.value != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = type != null ? type.hashCode() : 0;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        result = 31 * result + (value != null ? value.hashCode() : 0);
        result = 31 * result + (unit != null ? unit.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "Type : "+type.toString()
            + "// Name : "+name
            + "// Value : "+value
            + "// Unit : "+unit+".";
    }
}
